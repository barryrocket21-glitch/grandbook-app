#!/usr/bin/env node
/**
 * Phase 8H — Generate migration 061 from the SPX "Coverage area" XLSX.
 *
 * Reads the "NON Coverage SPX" sheet → distinct (state, city) pairs →
 * writes src/lib/supabase/migrations/061_spx_coverage_area.sql.
 *
 * Reproducible: re-run with a fresh file when SPX updates its coverage list.
 *   node scripts/gen-spx-coverage-migration.mjs "/path/to/Coverage area SPX.xlsx"
 */
import XLSX from 'xlsx'
import { writeFileSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const REPO_ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const SRC = process.argv[2] || `${process.env.HOME}/Downloads/Coverage area SPX (1).xlsx`
const SHEET = 'NON Coverage SPX'

const wb = XLSX.readFile(SRC)
if (!wb.Sheets[SHEET]) {
  console.error(`✗ Sheet "${SHEET}" not found in ${SRC}`)
  process.exit(1)
}
const rows = XLSX.utils.sheet_to_json(wb.Sheets[SHEET], { header: 1, defval: '' })

const norm = (v) => String(v || '').trim().toUpperCase().replace(/\s+/g, ' ')
const seen = new Set()
const pairs = []
for (const r of rows.slice(1)) {
  const s = norm(r[0])
  const c = norm(r[1])
  if (!s || !c) continue
  const k = `${s}||${c}`
  if (seen.has(k)) continue
  seen.add(k)
  pairs.push([s, c])
}
pairs.sort((a, b) => (a[0] + a[1]).localeCompare(b[0] + b[1]))

const esc = (v) => `'${v.replace(/'/g, "''")}'`
const values = pairs.map(([s, c]) => `    (${esc(s)}, ${esc(c)})`).join(',\n')

const sql = `-- 061 — Phase 8H: SPX coverage area (serviceability).
-- Generated by scripts/gen-spx-coverage-migration.mjs from the SPX
-- "Coverage area" XLSX, sheet "NON Coverage SPX" (${pairs.length} non-coverage
-- state+city pairs). Re-run the script with a fresh file when SPX updates.
--
-- master_wilayah_spx.is_serviceable: TRUE = SPX antar, FALSE = NOT SERVICEABLE.
-- Coverage is at state+city granularity → every district (kecamatan) row of a
-- non-coverage city is flagged. Idempotent: resets all to TRUE first.

UPDATE public.master_wilayah_spx SET is_serviceable = TRUE
WHERE is_serviceable IS NOT TRUE;

WITH non_coverage(state, city) AS (
  VALUES
${values}
)
UPDATE public.master_wilayah_spx m
SET is_serviceable = FALSE
FROM non_coverage nc
WHERE regexp_replace(UPPER(TRIM(m.state)), '\\s+', ' ', 'g') = nc.state
  AND regexp_replace(UPPER(TRIM(m.city)), '\\s+', ' ', 'g') = nc.city;
`

const out = join(REPO_ROOT, 'src/lib/supabase/migrations/061_spx_coverage_area.sql')
writeFileSync(out, sql)
console.log(`✓ Wrote ${out}`)
console.log(`  ${pairs.length} non-coverage (state, city) pairs across ${new Set(pairs.map((p) => p[0])).size} provinces`)
